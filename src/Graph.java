
/**
 * This class is used to represent a graph
 * @author Peng Liang (liangp@purdue.edu)
 *
 */
public class Graph {
	
	public int[][] g;
	
	/**
	 * Constructor
	 * @param m number of rows of the graph
	 * @param n number of columns of the graph
	 */
	public Graph(int m, int n) {
	g = new int[m][n];
	}
}
